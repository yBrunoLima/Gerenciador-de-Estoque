    private void btnPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisaActionPerformed
        String textoPesquisa = txtPesquisa.getText();
        if (textoPesquisa == null || textoPesquisa.trim().isEmpty()) {
            listar();
            return;
        }
        int cod = Integer.valueOf(textoPesquisa);
        MercadoriasDAO dao = new MercadoriasDAO();
        List<Mercadorias> lista = dao.FiltrarCOD(cod);
        DefaultTableModel dados = (DefaultTableModel) tabelaMercadoria.getModel();
        dados.setNumRows(0);
        for (Mercadorias m : lista) {
            dados.addRow(new Object[]{
            m.getId(),
            m.getDescricao(),
            m.getPreco(),
            m.getQntdEstoque(),
            m.getTamanho(),
            m.getModelo(),
            m.getFornecedor().getNome(),
            });
        }
        txtCodigo.setEnabled(false);
    }//GEN-LAST:event_btnPesquisaActionPerformed

