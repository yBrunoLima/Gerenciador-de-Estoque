    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        String nome = txtNomeProduto.getText();
        Mercadorias obj = new Mercadorias();
        MercadoriasDAO mdao = new MercadoriasDAO();
        obj = mdao.Buscar(nome);
        if (obj.getDescricao()!=null) {
            int estoque = Integer.valueOf(txtEstoque.getText());
            int quantidade = Integer.valueOf(txtQtd.getText());
            preco = Double.valueOf(txtPreco.getText());
            qtd = Integer.valueOf(txtQtd.getText());
            if (estoque>=quantidade) {
                subtotal = preco*qtd;
                total += subtotal;
                txtTotal.setText(String.valueOf(total));
                meus_produtos = (DefaultTableModel)tabelaCarrinho.getModel();
                meus_produtos.addRow(new Object[]{
                txtCodigo.getText(),
                txtNomeProduto.getText(),
                txtQtd.getText(),
                txtPreco.getText(),
                subtotal
                });
            }else{
                JOptionPane.showMessageDialog(null, "Estoque nao disponivel");
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "Item não adicionado");
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

