    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
        String nome = txtNomeCliente.getText();
        String cpf = txtCpf.getText();
        cliente = new Clientes();
        ClientesDAO cdao = new ClientesDAO();
        cliente = cdao.Buscar(nome);
        cliente = cdao.BuscarCpf(cpf);
        if (cliente.getNome()!=null && cliente.getCpf()!=null) {
            FormularioPagamento telap = new FormularioPagamento();
            telap.cliente = cliente;
            telap.meus_produtos = meus_produtos;
            telap.txtTotal.setText(String.valueOf(total));
            telap.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Campos faltantes");
        }
    }//GEN-LAST:event_btnPagarActionPerformed

