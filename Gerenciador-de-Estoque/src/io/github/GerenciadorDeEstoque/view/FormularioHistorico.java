    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        FormularioDetalheVenda fdv = new FormularioDetalheVenda();
        fdv.txtCodigo.setText(tabela.getValueAt(tabela.getSelectedRow(),0).toString());
        fdv.txtData.setText(tabela.getValueAt(tabela.getSelectedRow(),1).toString());
        fdv.txtTotal.setText(tabela.getValueAt(tabela.getSelectedRow(),2).toString());
        fdv.txtCliente.setText(tabela.getValueAt(tabela.getSelectedRow(),3).toString());
        int id = Integer.parseInt(fdv.txtCodigo.getText());
        ItensVendasDAO ivdao = new ItensVendasDAO();
        List<ItensVendas>lista = ivdao.listaItens(id);
        DefaultTableModel dados = (DefaultTableModel)fdv.tabela.getModel();
        for (ItensVendas i : lista) {
            dados.addRow(new Object[]{
            i.getMercadorias().getId(),
            i.getMercadorias().getDescricao(),
            i.getQnt(),
            i.getMercadorias().getPreco(),
            i.getSubtotal()
            });  
        }
        fdv.setVisible(true);
    }//GEN-LAST:event_tabelaMouseClicked

