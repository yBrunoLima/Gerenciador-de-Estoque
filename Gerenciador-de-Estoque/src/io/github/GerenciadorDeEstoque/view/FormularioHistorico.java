    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        DateTimeFormatter fmt = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate dataInicio = LocalDate.parse(txtDataInicio.getText(),fmt);
        LocalDate dataFim = LocalDate.parse(txtDataFim.getText(),fmt);
        VendasDAO vdao = new VendasDAO();
        List<Vendas>lista = vdao.Historico(dataInicio, dataFim);
        DefaultTableModel historico = (DefaultTableModel) tabela.getModel();
        historico.setNumRows(0);
        for (Vendas v : lista){
            historico.addRow(new Object[]{
            v.getId(),
            v.getData(),
            v.getTotal(),
            v.getCliente().getNome()

            });
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

